class Solution {
    public String replaceSpace(String s) {
        String[] val = s.split("");
        //遍历寻找有指定字符串的下标
        int i = -1;
        while (++i < val.length) {
            if (val[i].equals(" ")) {
                val[i] = "%20";
            }
        }
        StringBuffer sb = new StringBuffer();
        for(int j = 0; j < val.length; j++){
            sb. append(val[j] );
        }
       return sb.toString();
    }
}
